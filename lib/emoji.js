// Build a sentence with proper structure
export const buildSentence = (subject, verb, object, emotion = null) => {
  let sentence = `${subject} ${verb}`;
  if (object) sentence += ` ${object}`;
  if (emotion) sentence += ` ${emotion}`;
  return sentence;
};

// Make a question
export const makeQuestion = (statement) => `â“ ${statement}`;

// Make negative
export const makeNegative = (statement) => `ðŸš« ${statement}`;

// Add tense to verb
export const addTense = (verb, tense) => {
  const tenseMarkers = {
    past: 'â®ï¸',
    future: 'â­ï¸',
    continuous: 'ðŸ”„',
    completed: 'âœ…',
  };
  return `${verb}${tenseMarkers[tense] || ''}`;
};

// Conjugate verb (simplified emoji style)
export const conjugate = (verb, tense = 'present') => {
  if (grammar.verbForms[verb]) {
    return grammar.verbForms[verb][tense] || grammar.verbForms[verb].base;
  }
  return addTense(verb, tense);
};

// Build question
export const askQuestion = (type, subject = '') => {
  const q = grammar.questions[type] || 'â“';
  return subject ? `${q} ${subject}` : q;
};

// Join with conjunction
export const joinWith = (conjunction, ...parts) => {
  const conj = grammar.conjunctions[conjunction] || 'âž•';
  return parts.join(` ${conj} `);
};

// Add modifier
export const modify = (modifier, word) => {
  const mod = grammar.modifiers[modifier];
  return mod ? `${mod}${word}` : word;
};

// Complete sentence parser (text to emoji sentence)
export const parseToEmoji = (text) => {
  const words = text.toLowerCase().split(/\s+/);
  return words.map(word => {
    // Check all grammar categories
    if (grammar.pronouns[word]) return grammar.pronouns[word];
    if (grammar.conjunctions[word]) return grammar.conjunctions[word];
    if (grammar.prepositions[word]) return grammar.prepositions[word];
    if (grammar.modifiers[word]) return grammar.modifiers[word];
    if (grammar.questions[word]) return grammar.questions[word];
    if (dictionary[word]) return dictionary[word];
    return word;
  }).join(' ');
};

